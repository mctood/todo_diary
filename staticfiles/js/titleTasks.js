function randomTasks() {
	let localTasks = [...tasks]
	for (let i = 0; i < 30; i++) {
		let index = Math.floor(Math.random() * localTasks.length)
		let task = localTasks[index]
		localTasks.splice(index, 1)

		let taskDiv = `<div class="illustration-task">
<div class="task-icon">
<i class="fa fa-check"></i>
</div>
${task}
</div>`

		$(".title-illustration").append(taskDiv)
	}
}

function swipeAll() {

	let interval = setInterval(() => {
		let tasks = $(".illustration-task")
		if (tasks.length) {
			let task = $(tasks[0])
			task.addClass("swiped")
			$(task.find(".task-icon")).addClass("checked")
			$(".illustration-task:not(.swiped)").addClass("moved")
			setTimeout(() => {
				task.remove()
				tasks.removeClass("moved")
			}, 1000)
		}
		else {
			setTimeout(() => {
				startRoll()
			}, 2000)
			clearInterval(interval)
		}
	}, 2000)
}

function startRoll() {
	let roll = $("#roll")
	roll.show()
	roll[0].play()
}

$(() => {
	randomTasks()
	swipeAll()
})